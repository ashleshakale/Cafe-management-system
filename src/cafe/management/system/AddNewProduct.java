    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        ArrayList<Category> categoryList = CategoryDataAccessObject.getCategoryList();
        Iterator<Category> itr = categoryList.iterator();
        while(itr.hasNext()){
            Category category = itr.next();
            ComboBoxCategory.addItem(category.getName());
        }
    }//GEN-LAST:event_formComponentShown

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        Product product = new Product();
        product.setName(txtNewProductName.getText());
        product.setCategory((String)ComboBoxCategory.getSelectedItem());
        product.setPrice(txtProductPrice.getText());
        ProductDataAccessObject.saveProduct(product);
        new AddNewProduct(user).setVisible(true);
        setVisible(false);
    }//GEN-LAST:event_btnSaveActionPerformed

