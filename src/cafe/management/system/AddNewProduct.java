    public void validateFeild(){
        String name = txtNewProductName.getText();
        String price = txtProductPrice.getText();
        
        if(!name.isBlank() && !price.isBlank()){
            btnSave.setEnabled(true);
        }
        else{
            btnSave.setEnabled(false);            
        }
    }

