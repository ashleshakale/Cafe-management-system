    public void getAllUsers(String email) {
        DefaultTableModel dtm = (DefaultTableModel) tblVerifyUser.getModel();
        dtm.setRowCount(0);
        ArrayList<User> userList = UserDataAccessObject.getUsers(email);
        Iterator<User> itr = userList.iterator();
        while (itr.hasNext()) {
            User userObj = itr.next();
            if (userObj.getPrivilegeLevel() != null) {
                if (!userObj.getPrivilegeLevel().equals("Admin")) {        //??admin
                    dtm.addRow(new Object[]{userObj.getId(), userObj.getName(), userObj.getEmail(), userObj.getMobileNumber(), userObj.getAddress(), userObj.getSecurityQuestion(), userObj.getStatus()});
                }
            }
        }
    }

