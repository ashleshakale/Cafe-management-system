    private void tblVerifyUserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblVerifyUserMouseClicked
        // TODO add your handling code here:
        int index = tblVerifyUser.getSelectedRow();
        TableModel model = tblVerifyUser.getModel();
        String email = model.getValueAt(index, 2).toString();
        String status = model.getValueAt(index, 6).toString();
        if (status.equals("true")) {
            status = "false";
        } else {
            status = "true";
        }
        int a = JOptionPane.showConfirmDialog(null, "Do you want to change status of " + email + "", "Confirm", JOptionPane.YES_NO_OPTION);
        if (a == 0) {
            UserDataAccessObject.changeStatus(email, status);
            new VerifyUser(user).setVisible(true);
            setVisible(false);
        }
    }//GEN-LAST:event_tblVerifyUserMouseClicked

